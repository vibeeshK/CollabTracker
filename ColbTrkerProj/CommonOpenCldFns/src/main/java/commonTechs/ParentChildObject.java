package commonTechs;

/**
 * Convenience class to hold a parent and a child objects
 * 
 * @author Vibeesh Kamalakannan
 *
 */
public class ParentChildObject {
	/*
	 * Convenience class to hold a parent and a child objects
	 */
	public Object parentObj;
	public Object childObj;
	public ParentChildObject(Object inParentObj, Object inChildObj) {
		parentObj = inParentObj;
		childObj = inChildObj;
	}
}
